set_s = {10, 2, -3, 4, 5, 88}
print("The number of items in set s is : ", len(set_s))
print("The maximum element in set s is : ", max(set_s))
print("The minimum element is set s is : ", min(set_s))
print("The sum of all elements in the set s is : ", sum(set_s))
